package com.model;

public class Address {
	
 String state;
 String city;
 Long pin;
public String getState() {
	return state;
}
public void setState(String state) {
	this.state = state;
}
public String getCity() {
	return city;
}
public void setCity(String city) {
	this.city = city;
}
public Long getPin() {
	return pin;
}
public void setPin(Long pin) {
	this.pin = pin;
}

}
